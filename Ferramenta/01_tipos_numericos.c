#include <stdio.h>

int main() {
    // Declaração de variáveis numéricas
    int numeroInteiro = 10;
    float numeroDecimal = 3.14;

    // Exibir os valores
    printf("Número inteiro: %d\n", numeroInteiro);
    printf("Número decimal: %f\n", numeroDecimal);

    // Operações matemáticas básicas
    int soma = numeroInteiro + 5;
    float multiplicacao = numeroDecimal * 2.5;

    printf("Soma (int + 5): %d\n", soma);
    printf("Multiplicação (float * 2.5): %f\n", multiplicacao);

    return 0;
}
