#include <stdio.h>
#include <string.h>

int main() {
    // Declarando um caractere único
    char letra = 'A';

    // Declarando uma string (array de caracteres)
    char texto[] = "Olá, mundo!";

    // Exibir valores
    printf("Caractere: %c\n", letra);
    printf("String: %s\n", texto);

    return 0;
}
