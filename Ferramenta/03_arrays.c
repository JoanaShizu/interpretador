#include <stdio.h>

int main(void) {
    int myArray[3] = {6, 5, 4}; // Inicializa com valores inteiros
    float myFloats[2] = {1.5, 2.3}; // Inicializa com valores float
    char myChars[2] = {'a', 'b'}; // Inicializa com caracteres
    printf("Valor de myArray[1]: %d\n", myArray[1]);
    printf("Valor de myFloats[0]: %f\n", myFloats[0]);
    printf("Valor de myChars[1]: %c\n", myChars[1]);
return 0;
}
