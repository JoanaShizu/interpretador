int main() {
    int x = 2;

    switch (x) {
        case 1:
            printf("Valor de x é 1\n");
            break;
        case 2:
            printf("Valor de x é 2\n");
            break;
        case 3:
            printf("Valor de x é 3\n");
            break;
        default:
            printf("Valor de x não é 1, 2 ou 3\n");
    }

    return 0;
}
